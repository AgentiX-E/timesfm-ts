/**
 * Synthetic trend + seasonality pattern.
 *
 * Used as a sanity check: any reasonable forecast model should capture
 * this pattern's fundamental structure. Features:
 *   - Linear trend: +0.3 per step
 *   - 12-period seasonality (amplitude 15)
 *   - Ariane noise (±3)
 *
 * @param length Number of time steps (default 120)
 */
export function trendPlusSeasonality(length: number = 120): Float32Array {
  const arr = new Float32Array(length);
  const rng = mulberry32ForPattern(42);
  for (let i = 0; i < length; i++) {
    const trend = 50 + i * 0.3;
    const seasonal = 15 * Math.sin((2 * Math.PI * i) / 12);
    const noise = (rng() - 0.5) * 6;
    arr[i] = trend + seasonal + noise;
  }
  return arr;
}

/** Deterministic PRNG for reproducible synthetic patterns. */
function mulberry32ForPattern(seed: number): () => number {
  return () => {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

