export type RealWorldFixtureName = keyof typeof ALL_REAL_WORLD_FIXTURES;

/**
 * Get a slice of a real-world dataset for use in a test context.
 * Uses the last `contextLength` values as input and the next `horizon` values
 * as ground-truth for evaluation.
 */
export function getRealWorldTrainTest(
  name: RealWorldFixtureName,
  contextLength: number,
  horizon: number,
): { train: Float32Array; test: Float32Array } {
  const data = ALL_REAL_WORLD_FIXTURES[name];
  const totalLen = data.length;
  const splitPoint = Math.min(contextLength, totalLen - horizon);
  const train = new Float32Array(data.slice(0, splitPoint));
  const test = new Float32Array(data.slice(splitPoint, splitPoint + horizon));
  return { train, test };
}
